              <h3><a href="/">web测试之界面测试</a></h3>
              <p>所谓界面测试就是指，布局是否合理、整体风格是否一致、各个控件的放置位置是否符合客户使用习惯，此外还要测试界面操作便捷性、导航简单易懂性，页面元素的可用性，界面中文字是否正确，命名是否统一，页面是否美观，文字、图片组合是否完美等。</br>一般情况下我们进行界面测试，直接依据产品原型图以及UI切图/效果图，进行对比验证，确认是否一致。 但是如果碰到公司没有原型图，没有UI效果图怎么破？ 毕竟同是天涯沦落人的情况太多了~~</br>那么我们可以参考市场上同类型的成熟产品来做界面比对.</p>
